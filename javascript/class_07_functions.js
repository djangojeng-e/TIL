// functions 함수
// function 이라는 키워드를 사용
// 키워드 뒤에 함수 이름을 지정하고, 이름 안에 파라미터를 설정해줌
// return 이라는 키워드로, 값을 반환
// 아래 예시를 참조함.

function add (a, b) {
  return a + b;
}

const sum = add(1, 2);
console.log(sum);

function hello(name) {
  console.log('Hello, ' + name + '!');
}

hello('djangojenge');


