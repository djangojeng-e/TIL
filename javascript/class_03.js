// 연산자는 연산을 하기 위한 문자

// 산술 연산자 - 사칙 연산 작업을 하는 연산자
// 예 ) + - / *


let a = 1;
console.log(a++);
console.log(a);
console.log(++a);

// ++ 가 앞에 오느냐 뒤에 오느냐의 차이를 알아야 함
console.log(a--);
console.log(a)

// let a = 1 + 2 - (3 * 4) / 4
